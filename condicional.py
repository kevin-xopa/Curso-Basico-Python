number = int(input("Escribe un number... "))

""" The condition is a Boolean expression that evaluates to true ( True ) or false ( False ).
    The use of a colon (:) at the end of the condition is required. The code will be executed
    if the condition is met.
"""
if number > 5:
    print("es mayor a 5")
elif number == 5:
    print("es igual a 5")
else:
    print("es menor a 5")
